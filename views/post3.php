<div class="description2">Adults age 20 and older can interpret their BMI based on the following standard weight status categories. These are the same for men and women of all ages and body types:

BMI	Weight Status
Below 18.5	Underweight
18.5 – 24.9	Normal
25.0 – 29.9	Overweight
30.0 and above	Obese


